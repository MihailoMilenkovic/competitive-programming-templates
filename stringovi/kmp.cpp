vector<int> prefixFunction(string s){
    int n=s.size();
    vector<int>pi(n);
    for(int i=1;i<n;i++){
        int j=pi[i-1];
        while(j>0&&s[i]!=s[j]){
            j=pi[j-1];
        }
        if(s[i]==s[j])j++;
        pi[i]=j;
    }
    return pi;
}
int kmp(string pat,string text){
    string nw=pat+"#"+text;
    int matches=0;
    vector<int>pref=prefixFunction(nw);
    for(int i=pat.size()+1;i<=nw.size();i++){
        if(pref[i]==pat.size()){
            //cout<<"match on position "<<i<<"\n";
            matches++;
        }
    }
    return matches;
}
